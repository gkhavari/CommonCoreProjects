#include "libft.h"

void	*ft_calloc(size_t nmemb, size_t size)
{
	void	*r;
	size_t	total_size;

	total_size = nmemb * size;
	if (nmemb != 0 && total_size / nmemb != size)
		return (NULL);
	r = malloc(nmemb * size);
	if (r == NULL)
		return (NULL);
	ft_bzero(r, nmemb * size);
	return (r);
}
